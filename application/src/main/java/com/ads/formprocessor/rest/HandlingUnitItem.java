package com.ads.formprocessor.rest;

public class HandlingUnitItem {

	Integer HandlingUnitItemNumber;
	String HandlingUnitItemCode;
	String Material;
	
	public Integer getHandlingUnitItemNumber() {
		return HandlingUnitItemNumber;
	}
	public void setHandlingUnitItemNumber(Integer handlingUnitItemNumber) {
		HandlingUnitItemNumber = handlingUnitItemNumber;
	}
	public String getHandlingUnitItemCode() {
		return HandlingUnitItemCode;
	}
	public void setHandlingUnitItemCode(String handlingUnitItemCode) {
		HandlingUnitItemCode = handlingUnitItemCode;
	}
	public String getMaterial() {
		return Material;
	}
	public void setMaterial(String material) {
		Material = material;
	}
	
}
